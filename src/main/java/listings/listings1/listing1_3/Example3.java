package listings.listings1.listing1_3;

public class Example3 {
    public static void main(String[] args) {


        int var;
        double x;

        var = 10;
        x = 10.0;

        System.out.println("The initial value of variable var = " + var);
        System.out.println("The initial value of variable x = " + x);

        System.out.println();

        var = var / 4;
        x = x / 4;

        System.out.println("Variable var after divide = " + var);
        System.out.println("Variable x after divide = " + x);


    }
}
